#include <iostream>
using namespace std;

int main()
{
    int a = 5, b = 4;
    if (a == b)
    {
        cout << "a is equal to b" << endl;
    }
    if (a > b)
    {
        cout << "a is greater than b" << endl;
    }
    if (a < b)
    {
        cout << "a is less than b" << endl;
    }
    if (a >= b)
    {
        cout << "a is greater than or equal to b" << endl;
    }
    if (a <= b)
    {
        cout << "a is less than or eqaul to b" << endl;
    }

    if (a != b)
    {
        cout << "a is not greater than b" << endl;
    }
    return 0;
}